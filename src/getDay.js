export function getDay(n) {
    if (n === 1) return 'Monday';
    if (n === 2) return 'Tuesday';
    if (n === 3) return 'Wedneday';
    if (n === 4) return 'Thurday';
    if (n === 5) return 'Friday';
    if (n === 6) return 'Saturday';
    if (n === 7) return 'Sunday';
    
    return 'неизвестно';
}



